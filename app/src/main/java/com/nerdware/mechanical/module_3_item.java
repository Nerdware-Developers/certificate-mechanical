package com.nerdware.mechanical;

public class module_3_item {
    int image;
    String m3;
    String p3;

    public module_3_item(int image, String m3, String p3) {
        this.image = image;
        this.m3 = m3;
        this.p3 = p3;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getM3() {
        return m3;
    }

    public void setM3(String m3) {
        this.m3 = m3;
    }

    public String getP3() {
        return p3;
    }

    public void setP3(String p3) {
        this.p3 = p3;
    }
}
